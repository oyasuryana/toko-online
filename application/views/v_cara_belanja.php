<h2 class="text-center text-success  font-weight-bold mt-4">Panduan Belanja Online</h2>

<p class="font-weight-bold">A. Tata Cara Belanja</p>
<p>Untuk pelayanan optimal silahkan ikuti panduan belanja online berikut :</p>
<ol>
	<li>Untuk berbelanja silahkan baca informasi produk selengkapnya dengan menekan tombol detail pada setiap barang.</li>
	<li>Untuk berbelanja silahkan buat akun dengan cara melakukan pendaftaran</li> 
	<li>Setelah memiliki akun, silahkan login untuk melakukan pembelian</li>
	<li>Setelah berhasil login silahkan lakukan pembelian</li>
	<li>Apabila telah selesai memilih barang, silahkan klik tombol Chekout</li>
	<li>Selanjutnya pilih rekening bank tujuan untuk mentransfer pembayaran</li>
	<li>Apabila telah melakukan transfer, lakukan konfirmasi dengan meng-upload bukti transfer di menu konfirmasi</li>
	<li>Apabila sudah meng-upload tunggu beberapa saat, jika anda menerima kode resi pengiriman di halaman pesanan berarti pesanan anda sudah dikirim</li>	
</ol>

<p class="font-weight-bold">B. Komplain Atas Pelayanan</p>
<p>Jika merasa mendapat pelayanan kurang memuaskan, barang mengalami kerusakan atau ketidakpuasan lainnya silahkan ikuti aturan berikut :</p>

<ol>
<li>Sebagai pembeli, Anda dapat menjelaskan kendala transaksi yang terjadi dan solusi apa yang diinginkan.</li>
<li>Saat mengajukan komplain, Anda wajib melampirkan bukti pendukung, seperti kemasan produk, foto produk dari berbagai sisi, nota pemesanan, dan resi penerimaan.</li>
<li>Kami selanjutnya akan menanggapi keluhan, memberikan penjelasan, serta menerima solusi atau menawarkan solusi baru.</li>
<li>Setelah Anda menyetujui dan melakukan solusi yang sudah disepakati, komplain akan dianggap selesai. </li>
</ol>	
